<h1>Comment se préparer aux tests d'entrée de la formation d'EPL/S ?</h1>
<img src="https://www.sup-admission.com/_ima-site/logo-concours-enac.png" class="logo1" alt="Prépa concours Enac" />
<p><b>Le pilote de ligne</b> fait partie du personnel naviguant technique (PNT).</p>
<p>Avant le décollage, il prépare le plan de vol (en prenant en compte la météo, le poids de l'avion et le distance à parcourir) et les instruments de navigation. Pendant le vol, le pilote de ligne assure la sécurité des passagers, surveille le tableau de bord et maintient une liaison permanente avec les tours de contrôle. En raison des évolutions technologiques permanentes dans l'aviation, il se forme tout au long de sa carrière.</p>
<p>Au sein de l'École nationale d'aviation civile (ENAC), il existe plusieurs voies, en fonction du cursus, pour devenir pilote de ligne. Parmi celles-ci, la formation EPL/S, accessible sur concours, ne requiert aucune connaissance et compétence aéronautique.</p>
