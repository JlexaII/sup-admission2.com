<h2>Quel est le programme de cette formation Élève Pilote de Ligne ?</h2>
<p>La durée de la formation est de 24 mois :</p>
<ul>
<li>Au programme de la formation théorique : Mécanique du vol, Procédures opérationnelles, Navigation, Météorologie, Performances humaines et ses limites, Performances et préparation du vol, Connaissances générales des aéronefs, Droit aérien, Communication...</li>
<li>Pendant la formation pratique, les élèves préparent la licence de pilote professionnel avion (CPL A), assortie de la qualification de vol aux instruments (IR) sur avion multi moteurs, et du certificat de formation au travail en équipage (MCC).</li>
</ul>
<p>Après la formation et après un stage de qualification effectué dans la compagnie aérienne d'accueil, vous serez copilote pendant plusieurs années, avant d'accéder à la fonction de commandant de bord.</p>
