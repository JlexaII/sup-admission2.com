<div id="tabs-2">
<h2>Quelles sont les conditions pour concourir ?</h2>
<p>Pour candidater à cette formation de pilote de ligne EPL/S, il n'est pas nécessaire d'avoir des connaissances ou des compétences aéronautiques. Contrairement aux deux autres concours d'Élève Pilote de Ligne qui s'adressent à des candidats ayant des connaissances théoriques (EPL/U) ou des connaissances théoriques et pratiques (EPL/P).</p>
<p>Pour être admis, vous devez :</p>
<ul>
<li>Avoir plus de 16 ans et moins de 23 ans au 1 er janvier de l'année du concours,</li>
<li>Justifier de 60 crédits ECTS (Bac +1), obtenus dans une formation scientifique ou technique,</li>
<li>Être ressortissant de la Communauté Européenne, en situation régulière au regard du service national et reconnu physiquement apte à l'emploi.</li>
</ul>
</div>
